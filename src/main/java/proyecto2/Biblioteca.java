    private void jToggleButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton4ActionPerformed
        // TODO add your handling code here:
        Proyecto2.listaDoble.delete(jComboBox1.getSelectedItem());
        
        jComboBox1.removeAllItems();
        Nodo actual = (Nodo) Proyecto2.listaDoble.peek();
        while (actual != null) {
            if ((actual.getDato()) != null) {
                    jComboBox1.addItem(actual.getDato().toString());
            }
        actual = actual.getAnterior();
        }
        
        if (imagenActual.getAnterior() != null) {
            imagenActual = imagenActual.getAnterior();
                                         
            Image imagen = new ImageIcon((String) imagenActual.getDato()).getImage();
            ImageIcon icono = new ImageIcon(imagen.getScaledInstance(jLabel3.getWidth(), jLabel3.getHeight(), Image.SCALE_SMOOTH));
            jLabel3.setIcon(icono);
        }
        if (imagenActual.getSiguiente()!= null) {
            imagenActual = imagenActual.getSiguiente();
                    
            Image imagen = new ImageIcon((String) imagenActual.getDato()).getImage();
            ImageIcon icono = new ImageIcon(imagen.getScaledInstance(jLabel3.getWidth(), jLabel3.getHeight(), Image.SCALE_SMOOTH));
            jLabel3.setIcon(icono);

        }
        if (imagenActual.getSiguiente()== null) {
            ruta = "";
                    
            Image imagen = new ImageIcon(ruta).getImage();
            ImageIcon icono = new ImageIcon(imagen.getScaledInstance(jLabel3.getWidth(), jLabel3.getHeight(), Image.SCALE_SMOOTH));
            jLabel3.setIcon(icono);

        }
        repaint();
    }//GEN-LAST:event_jToggleButton4ActionPerformed

