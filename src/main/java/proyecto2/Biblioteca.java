    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        // TODO add your handling code here:
                JFileChooser fileChooser = new JFileChooser();
                fileChooser.setMultiSelectionEnabled(true);
                int seleccion = fileChooser.showOpenDialog(this);

                if (seleccion == JFileChooser.APPROVE_OPTION) {
                    File[] Imagenes = fileChooser.getSelectedFiles();
                    
                    for (File Imagene : Imagenes) {
                        Proyecto2.listaDoble.add(Imagene.getAbsolutePath());

                    }
                    imagenActual = (Nodo) Proyecto2.listaDoble.peek();
                    ruta = fileChooser.getSelectedFile().getAbsolutePath();
                    
                    Image imagen = new ImageIcon(ruta).getImage();
                    ImageIcon icono = new ImageIcon(imagen.getScaledInstance(jLabel3.getWidth(), jLabel3.getHeight(), Image.SCALE_SMOOTH));
                    jLabel3.setIcon(icono);
                    
                    repaint();
                    
                }
                jComboBox1.removeAllItems();
                Nodo actual = (Nodo) Proyecto2.listaDoble.peek();
                while (actual != null) {
                    if ((actual.getDato()) != null) {
                        jComboBox1.addItem(actual.getDato().toString());
                    }
                actual = actual.getAnterior();
                }
                
    }//GEN-LAST:event_jToggleButton3ActionPerformed

